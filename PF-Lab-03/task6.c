#include <stdio.h>

int main() {
    
    int Int = 98;               
    float Float = 2.36f;       
    double Double = 6.69856; 
    char Char = 'O';            

    printf("Integer: %d\n", Int);
    printf("Float: %f\n", Float);
    printf("Double: %lf\n", Double);
    printf("Char: %c\n", Char);

    return 0;
}

